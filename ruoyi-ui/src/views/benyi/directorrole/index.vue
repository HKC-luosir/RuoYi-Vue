              教练，教练，指导示范。作为教练，核心的能力是“示范”。最优秀的教练，就是把每一条标准都能够亲自做出示范，并在示范的基础上做出充分的指导。而示范是基础，是前提，是必要条件；指导只是充分条件，起到辅助作用。在实践中，不能做出“示范”的指导，体现为“外行领导内行”，会被诟病为“站着说话不腰疼”而无法服众，从而收不到积极与理想的效果；但不能“指导”的示范，则展示不了专业功底，因此也会在一定程度上使效果打折扣。园长充当教练，可以实现几个在管理上非常必要的功能。
