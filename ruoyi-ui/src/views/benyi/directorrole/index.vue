            <p class="text">
              “算盘”，是精明的俗称，是“好管家”的别名，与“门儿精”同义。尽管也有“抠门儿”的涵义，但把这个词用于形容管理一个像幼儿园这样的小组织、小生意或曰“小企业”的话，我敢说其褒义大于贬义。无论在哪一种意义上，一所幼儿园都不能算是一个中或大组织（如按照国际通用的分类，雇员在500人之内的都属于小企业）。既然是小组织，必然是“小本经营”。与公立幼儿园的“照行政拨款悉数花钱”不同，私立园在市场中生存的现实，迫使做园长的必须当好精明的“算盘”。在管理学上，良性经营，或者说“盈余”（在当前法律框架下，人们都忌讳“利润”这个字眼儿）的产生无非就是两个途径：开源与节流。尽管在运营中，开源与节流都很重要，但对于幼儿园的经营管理，开源无论怎样都是有限度的。然而，节流则表现为相对“无休止”。这一原理也得到了经济学与管理学上的证明。俗话说，“算计不到才受穷。”如果“外面挣来了一扇门”，而“家里丢了一块板子”，这样的管理能够让“盈余”最大化吗？想当好“门儿精”的“算盘”，园长必须是一个优秀的预算管理专家。首先，幼儿园建立一套适宜的预算管理制度是非常必要的。其中需要严格规定：怎样花钱，花多少钱，花钱买什么,花多了的申请程序是什么，节省了的奖励怎样，花钱申请与报销的程序如何，花钱人的权限多大，等等，都必须有巨细的、详尽的制度规定。其次，一定要把握几个原则。第一个原则，该花的一定要花，而且必须花。比如，绝对不可以从孩子们的“嘴里”省钱，因为那等同于“犯罪”；法律规定的教职工保险及福利不能“抠门儿”；再比如，必要的设施、设备、玩教具与各种教学材料、教师必要的培训与工作必须的工具及设备，等等，不能太算计。此外，业绩奖励也是绝对不可以“节省”的。第二个原则，花在刀刃上。有些钱花出去可能很有必要，但如果在轻重缓急上排名靠后的话，可以不花，而是花在那些必须得花的项目上。第三个原则，量入为出。即便是必须买的东西，但现金流限制，也可以考虑拖后（在幼儿园正常运转的条件下，这种情形经常出现。但建园初期情况不同）。最后一点，为作为园长的你界定一个“安慰”：“抠门儿”未必不是好园长。
            </p>
