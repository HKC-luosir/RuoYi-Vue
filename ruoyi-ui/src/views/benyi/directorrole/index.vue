              楷模者，榜样也。作为园长，你要求员工做到的，你必须自己能做到，而且必须一如既往、始终如一的做到，而不仅仅是“做秀”，不管是职业园长，还是老板园长。首先，作为“榜样”，园长责无旁贷。你没有理由推托作为榜样的责任。换言之，在幼儿园管理中，你必须全方位地扮演榜样的角色。第二，榜样是无声的“督导”。无论在哪一方面，只要园长将自己对员工的要求，用榜样的方式展示自己的态度与行为，那么，这就是一种无声的督导。它让跟随者没有理由推脱自己“没能做好”的责任。因为，园长的榜样行为就是一种无形的监督和检查。第三，榜样的力量是无穷的。榜样的奇妙不仅在于它可以在无声息中发上作用，更重要的是，它的力量是无穷的。由于榜样力量对他人产生的影响方向往往是由内向外的，因而我们断定这种力量完全属于精神层面的。而精神力量是无法衡量的，因其转变成的物质也就同样是巨大与无法计算的。因此第四，榜样是园所文化的重要组成部分。以价值、态度、行为为核心内容的园所文化，其构建、形成以及发挥作用的过程，起着关键作用的要素多半都属于精神层面的。其中以态度与行为为基本表现力的榜样作用，功不可没。在某种程度上，园长身上表现出来的榜样本身就是权威。
            </p>
