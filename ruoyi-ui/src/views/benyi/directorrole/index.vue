              第一，树立权威。能在包括核心课程组织实施、教学、师童互动、家长工作等幼儿园教育诸多日常工作层面上，为自己的员工做出规范的示范指导，无论从哪一方面来说，都是树立自己领导权威条件和契机。
            </p>
            <p class="text">
              第二，保持高品质。幼儿园教职工，尤其是教师的经常流动不可避免。新招聘进园的教师，无论是否有工作经验，都必须熟悉和掌握本幼儿园确立的理念、体系和操作流程。在此种情况下，任何其他人的培训和指导，都远远比不上园长亲自披挂上阵来示范指导的作用大和效果好。因为这样既能够激起和保持学习者的积极性，同时也是增进与员工了解与感情的绝好机会。如果能将“园长示范”作为制度执行的话，那么，这无疑应当是从人力资源角度保持幼儿园教育高品质的最佳管理策略。
            </p>
            <p class="text">
              第三，提高执行力，避免官僚与内耗。在园长与员工之间增加任何管理层次，都必定会导致某种程度的官僚问题与内耗产生，由此必然导致执行力打折扣。因此，在某些非常关键培训中，园长的亲历亲为是极为必要的。未来的园长不可能再由“外行”来充当。能够充当优秀专业教练的园长，是发展的必然趋势和方向。
