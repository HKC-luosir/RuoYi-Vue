        // 表单参数
        form: {},
        // 表单校验
        rules: {
          roomId: [{
            required: true,
            message: "房间id不能为空",
            trigger: "blur"
          }],
          userId: [{
            required: true,
            message: "使用人id不能为空",
            trigger: "blur"
          }],
          startTime: [{
            required: true,
            message: "开始时间不能为空",
            trigger: "blur"
          }],
          endTime: [{
            required: true,
            message: "结束时间不能为空",
            trigger: "blur"
          }],
        },
        storeOptions: [],
        roomOptions: [],
      };
    },
    created() {
      this.getList();
      this.getStoreOption();
      this.getRoomOption();
    },
    methods: {
      /** 查询房间占用（点支付时再次校验可用性并改变状态，支付失败回滚）列表 */
      getList() {
        this.loading = true;
        listRoomorder(this.queryParams).then(response => {
          this.roomorderList = response.rows;
          this.total = response.total;
          this.loading = false;
        });
      },
      getStoreOption() {

      },
      getRoomOption() {

      },
      // 取消按钮
      cancel() {
        this.open = false;
        this.reset();
      },
      // 表单重置
      reset() {
        this.form = {
          id: null,
          roomId: null,
          userId: null,
          startTime: null,
          endTime: null,
          totalAmount: null,
          payAmount: null,
          couponAmount: null,
          couponId: null,
          status: null,
          remark: null,
          createBy: null,
          createTime: null,
          updateBy: null,
          updateTime: null
        };
        this.resetForm("form");
      },
      /** 搜索按钮操作 */
      handleQuery() {
        this.queryParams.pageNum = 1;
        this.getList();
      },
      /** 重置按钮操作 */
      resetQuery() {
        this.resetForm("queryForm");
        this.handleQuery();
      },
      // 多选框选中数据
      handleSelectionChange(selection) {
        this.ids = selection.map(item => item.id)
        this.single = selection.length !== 1
        this.multiple = !selection.length
      },
      /** 新增按钮操作 */
      handleAdd() {
        this.reset();
        this.open = true;
        this.title = "添加预约";
      },
      /** 修改按钮操作 */
      handleUpdate(row) {
        this.reset();
        const id = row.id || this.ids
        getRoomorder(id).then(response => {
          this.form = response.data;
          this.open = true;
          this.title = "修改房间占用（点支付时再次校验可用性并改变状态，支付失败回滚）";
        });
      },
      /** 提交按钮 */
      submitForm() {
        this.$refs["form"].validate(valid => {
          if (valid) {
            if (this.form.id != null) {
              updateRoomorder(this.form).then(response => {
                this.$modal.msgSuccess("修改成功");
                this.open = false;
                this.getList();
              });
            } else {
              addRoomorder(this.form).then(response => {
                this.$modal.msgSuccess("新增成功");
                this.open = false;
                this.getList();
              });
            }
          }
        });
      },
      /** 删除按钮操作 */
      handleDelete(row) {
        const ids = row.id || this.ids;
        this.$modal.confirm('是否确认删除房间占用（点支付时再次校验可用性并改变状态，支付失败回滚）编号为"' + ids + '"的数据项？').then(function() {
          return delRoomorder(ids);
        }).then(() => {
          this.getList();
          this.$modal.msgSuccess("删除成功");
        }).catch(() => {});
      },
      /** 导出按钮操作 */
      handleExport() {
        this.download('office/roomorder/export', {
          ...this.queryParams
        }, `roomorder_${new Date().getTime()}.xlsx`)
      }
    }
  };
</script>
